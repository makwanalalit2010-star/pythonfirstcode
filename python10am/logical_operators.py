'''mplement a program to demonstrate logical operators (and, or, not) by asking the user for boolean inputs (e.g., true/false values).
'''
a = input("enter first value(true/false):") == "true"
b = input("enter second value(true/false):") == "false"

print("a and b:", a and b)
print("a or b:", a or b)
print("not a:", not a)
print("not b:", not b)
