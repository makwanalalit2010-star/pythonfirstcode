'''Write a program to demonstrate assignment operators (=, +, -, *-, /=) using a single variable.'''

a = 10
print("value of a:", a)

a += 6
print("value of a after a +=6:", a)

a -= 4
print("value of a after a -=4:", a)

a *= 2
print("value of a after a *=2:", a)

a /= 4
print("value of a after a /=4:", a)

