'''Declares variables of different datatypes (integer, float, string, boolean, complex, etc.).

Prints their values and types using the type() function.

Create a program where the user inputs their height and weight.
'''
integer = 10
floating = 10.9
string = "lalit"
boolean = True

print("integer:", integer)
print("type:", type(integer))

print("float:", floating)
