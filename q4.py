'''Write a Python program that:

- Declares variables of different datatypes (integer, float, string, boolean, complex, etc.).

- Prints their values and types using the type() function.'''

num = 25
price = 99.15
name = "lalit"
complex_num = 3 + 4j

print("integer value:",num)
print("type:",type(num))

print("float value:",price)
print("type:",type(price))

print("string value:",name)
print("type:",type(name))

print("complex value:",complex_num)
print("type:",type(complex_num))

